CREATE TABLE ALUNO (
    RA NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    NOME VARCHAR(100),
    CURSO VARCHAR(100),
    EMAIL VARCHAR(100),
    );

CREATE TABLE LIVRO(
    ISBN NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    TITULO VARCHAR(100),
    ano_publicacao INT(4),

);

CREATE TABLE AUTOR(
    id_autor INT PRIMARY KEY,
    NOME VARCHAR(100),
    NACIONALIDADE VARCHAR(100),

);

CREATE TABLE EXEMPLAR(
    id_exemplar INT PRIMARY KEY,
    ESTADO VARCHAR(100),
    disponibilidade bool,
     
);